public class HolaMundo {    //Identificador de clase pública
	public static void main(String[] args) {	// Comienzo del programa
		System.out.println("Hola mundo...");  // Muestra mensaje 

        System.out.println("HolaMundo, Programa original de AJP, versión 2"); // Muestra mensaje
	}
}

 /**
  * En este código, las palabras reservadas son public, class, static, void, main y String
  * Los delimitadores son ;, (, ), {, }, ", .
  * Al comienzo del código se encuentra el identificador de clase pública, que lleva
  * el mismo nombre que el archivo (HolaMundo), se abre una llave. Mas abajo se encuentra main, que es el identificador del
  * comienzo del programa. Antes de main, se encuentran las palabras reservadas public static y void que indican que
  * el programa tiene acceso público, es estático y no devuelve ningún valor, se abre otra llave con el programa en sí.
  * Main recibe como parámetro cadenas de caracteres (String[] args). Dentro del programa, se encuentra la sentencia
  * System.out.println, que muestra un mensaje en la consola del sistema. El mensaje que se muestra es
  * "Hola mundo...", delimitado por dobles comillas, es una cadena de caracteres. Se cierra la sentencia con ; y se cierran
  * las llaves.
  *
  * Las líneas desplazadas se deben a que el código está escrito en una estructura que favorece la legibilidad
  * realmente no es necesario que estén desplazadas. los editores de código e IDEs lo suelen hacer automáticamente.
  * Las palabras que se escriben con mayúscula la primera letra(Como System o String) son clases.
  * Si se cambian a minúscula, el programa no compilará.
  */